package com.noir.menu.vo;

public class WineVO {
	
	int wine_id;
	String wine_name;
	String wine_type;
	String region;
	String vintage;
	String producer;
	String price;
	String bottle_size;
	String description;
	String wine_category;
	
	public int getWine_id() {
		return wine_id;
	}
	public void setWine_id(int wine_id) {
		this.wine_id = wine_id;
	}
	public String getWine_name() {
		return wine_name;
	}
	public void setWine_name(String wine_name) {
		this.wine_name = wine_name;
	}
	public String getWine_type() {
		return wine_type;
	}
	public void setWine_type(String wine_type) {
		this.wine_type = wine_type;
	}
	public String getRegion() {
		return region;
	}
	public void setRegion(String region) {
		this.region = region;
	}
	public String getVintage() {
		return vintage;
	}
	public void setVintage(String vintage) {
		this.vintage = vintage;
	}
	public String getProducer() {
		return producer;
	}
	public void setProducer(String producer) {
		this.producer = producer;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public String getBottle_size() {
		return bottle_size;
	}
	public void setBottle_size(String bottle_size) {
		this.bottle_size = bottle_size;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getWine_category() {
		return wine_category;
	}
	public void setWine_category(String wine_category) {
		this.wine_category = wine_category;
	}
	
}
